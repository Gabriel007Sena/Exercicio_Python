"""
02 .Efetuar a leitura de dois valores numéricos inteiros representados pelas variáveis A e B e apresentar o resultado da diferença A-B.
"""
# Entrada de Dados 
A = int(input("Digite o valor de A: "))
B = int(input("Digite o valor de B: "))

# Processamento 
diferenca = A - B

# Saida
print("A diferença entre A e B é:", diferenca)
