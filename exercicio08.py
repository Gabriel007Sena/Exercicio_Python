"""
08. Faça um algoritmo para calcular quantas ferraduras são necessárias para equipar todos os cavalos comprados para um haras.
"""
# Entrada de Dados 
num_cavalos = int(input("Digite o número de cavalos comprados: "))

# Processamento
num_ferraduras = num_cavalos * 4

# Saida de Dados
print("Serão necessárias", num_ferraduras, "ferraduras para equipar todos os cavalos.")

